import streamlit as st
import numpy as np
import plotly.graph_objects as go
import io
import os

# ──────────────────────────────────────────────────────────────────────────────
# 1. PARSING DU FICHIER DE POINTS
# ──────────────────────────────────────────────────────────────────────────────

def parse_points(content: str) -> tuple[list, list, list]:
    """
    Parse le contenu texte d'un fichier de points CloudCompare.
    Ignore la première ligne (header) et ne garde que X, Y, Z.

    Returns
    -------
    (x, y, z) : trois listes de floats
    """
    x, y, z = [], [], []
    lines = content.splitlines()
    for line in lines[1:]:       # skip header
        parts = line.split()
        if len(parts) < 3:
            continue
        try:
            x.append(float(parts[0]))
            y.append(float(parts[1]))
            z.append(float(parts[2]))
        except ValueError:
            continue
    return x, y, z


# ──────────────────────────────────────────────────────────────────────────────
# 2. GÉNÉRATION DU SCRIPT SALOMÉ
# ──────────────────────────────────────────────────────────────────────────────

def generate_salome_script(x: list, y: list, z: list, nom: str) -> str:
    """
    Génère le contenu du fichier .py compatible Salomé.

    Parameters
    ----------
    x, y, z : coordonnées des points
    nom     : nom de base du fichier (sans extension)

    Returns
    -------
    str : contenu complet du script Python Salomé
    """
    lines = []

    # En-tête
    lines += [
        "#!/usr/bin/env python\n",
        "###\n",
        f"### Generated by Salomé Generator — {nom}\n",
        "###\n",
        "import sys\n",
        "import salome\n",
        "salome.salome_init()\n",
        "import salome_notebook\n",
        "notebook = salome_notebook.NoteBook()\n",
        "###\n",
        "### GEOM component\n",
        "###\n",
        "import GEOM\n",
        "from salome.geom import geomBuilder\n",
        "import math\n",
        "import SALOMEDS\n",
        "geompy = geomBuilder.New()\n",
        "O  = geompy.MakeVertex(0, 0, 0)\n",
        "OX = geompy.MakeVectorDXDYDZ(1, 0, 0)\n",
        "OY = geompy.MakeVectorDXDYDZ(0, 1, 0)\n",
        "OZ = geompy.MakeVectorDXDYDZ(0, 0, 1)\n",
    ]

    # Vertices
    for i, (xi, yi, zi) in enumerate(zip(x, y, z), 1):
        lines.append(f"Vertex_{i} = geompy.MakeVertex({xi}, {yi}, {zi})\n")
        lines.append(f"geompy.addToStudy(Vertex_{i}, 'Vertex_{i}')\n")

    # Pied de page
    lines += [
        "geompy.addToStudy( O,  'O'  )\n",
        "geompy.addToStudy( OX, 'OX' )\n",
        "geompy.addToStudy( OY, 'OY' )\n",
        "geompy.addToStudy( OZ, 'OZ' )\n",
        "if salome.sg.hasDesktop(): salome.sg.updateObjBrowser()\n",
        "### Fin du script\n",
    ]

    return "".join(lines)


# ──────────────────────────────────────────────────────────────────────────────
# 3. GRAPHIQUE 3D
# ──────────────────────────────────────────────────────────────────────────────

def plot_3d(x: list, y: list, z: list) -> go.Figure:
    """
    Retourne une figure Plotly du nuage de points 3D.
    """
    fig = go.Figure(data=[go.Scatter3d(
        x=x, y=y, z=z,
        mode="markers",
        marker=dict(
            size=2,
            color=z,
            colorscale="Viridis",
            opacity=0.8,
            colorbar=dict(title="Z"),
        ),
    )])

    fig.update_layout(
        scene=dict(
            xaxis_title="X",
            yaxis_title="Y",
            zaxis_title="Z",
            aspectmode="data",
        ),
        margin=dict(l=0, r=0, t=40, b=0),
        title="Nuage de points 3D",
        height=600,
        template="plotly_white",
    )
    return fig


