def main():
    st.set_page_config(
        page_title="Générateur dump Salomé",
        page_icon="🔷",
        layout="wide",
    )

    # ── En-tête ───────────────────────────────────────────────────────────────
    st.title("🔷 Générateur de dump Salomé — Nuage de points 3D")
    st.markdown("Fait avec ♥ par Thibaud*")

    with st.expander("📟 Logo ASCII", expanded=False):
        st.code(BANNER, language=None)

    st.markdown("---")

    # ── Instructions ──────────────────────────────────────────────────────────
    with st.expander("📋 Mode d'emploi", expanded=True):
        st.markdown(
            """
1. **Enregistrer** les points au format `.txt` depuis CloudCompare ;
2. **Charger** le fichier avec le bouton ci-dessous ;
3. Le script `.py` Salomé est généré et téléchargeable directement ;
4. **Ouvrir** ce fichier dans Salomé via un dump (`Ctrl + T` dans Salomé).

> ⚠️ **Attention** : si plus de 5 000 points, Salomé peut avoir des difficultés
> à importer le dump et corrompre la géométrie. Un avertissement s'affiche au-delà
> de 2 000 points.

            """
        )

    # ── Upload ────────────────────────────────────────────────────────────────
    uploaded = st.file_uploader(
        "📂 Sélectionner un fichier de points (.txt)",
        type=["txt"],
        help="Fichier exporté depuis CloudCompare — format : X Y Z (+ colonnes optionnelles ignorées)",
    )

    if uploaded is None:
        st.info("En attente d'un fichier…")
        return

    # ── Parsing ───────────────────────────────────────────────────────────────
    content = uploaded.read().decode("utf-8", errors="replace")
    x, y, z = parse_points(content)
    n = len(x)

    if n == 0:
        st.error("Aucun point valide trouvé dans le fichier. Vérifier le format (X Y Z par ligne).")
        return

    # ── Info points ───────────────────────────────────────────────────────────
    if n > 2000:
        st.warning(
            f"⚠️ **{n} points détectés** : quantité importante, "
            "Salomé risque d'avoir des difficultés à importer le dump."
        )
    else:
        st.success(f"✅ **{n} points détectés** : quantité raisonnable, import Salomé sans risque particulier.")

    # ── Statistiques rapides ──────────────────────────────────────────────────
    with st.expander("📊 Statistiques du nuage", expanded=False):
        col1, col2, col3 = st.columns(3)
        for col, vals, label in zip([col1, col2, col3], [x, y, z], ["X", "Y", "Z"]):
            col.metric(f"{label} min", f"{min(vals):.4f}")
            col.metric(f"{label} max", f"{max(vals):.4f}")
            col.metric(f"{label} étendue", f"{max(vals) - min(vals):.4f}")

    # ── Graphique 3D ─────────────────────────────────────────────────────────
    st.subheader("📊 Visualisation 3D")
    fig = plot_3d(x, y, z)
    st.plotly_chart(fig, use_container_width=True)

    # ── Génération du script Salomé ───────────────────────────────────────────
    st.subheader("⚙️ Script Salomé généré")
    nom = os.path.splitext(uploaded.name)[0]
    script = generate_salome_script(x, y, z, nom)

    nb_lignes = script.count("\n")
    st.markdown(f"Script généré : **{nb_lignes} lignes**, **{n} vertices**.")

    with st.expander("👁️ Aperçu du script (50 premières lignes)", expanded=False):
        preview = "\n".join(script.splitlines()[:50]) + "\n..."
        st.code(preview, language="python")

    # ── Téléchargement ────────────────────────────────────────────────────────
    st.download_button(
        label="📥 Télécharger le script Salomé (.py)",
        data=script.encode("utf-8"),
        file_name=f"{nom}.py",
        mime="text/x-python",
        use_container_width=True,
        type="primary",
    )

    st.markdown("---")
    st.caption("Générateur de dump Salomé · CloudCompare → Salomé GEOM · 🔷")


if __name__ == "__main__":
    main()
